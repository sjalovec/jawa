public class TridaMATH {
    public static void main(String[] args) {
        int a = 2;
        double b = Math.pow(a, 3); // 2 ^ 3 = 8
        System.out.println(b);
        
        double e = Math.E; //Eulerovo číslo
        System.out.println(e);
        
        int i = (int)Math.sqrt(4); //druhá odmocnina ze 4. standardne vraci double. Přetypováno na int.
        System.out.println(i);
    }

}
