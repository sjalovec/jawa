package Objekty;

public class CviceniOdTJ {
    public static void main(String[] args) {
        System.out.println("");
        int n = 5;
        for (int i = 0; i < n; i++)
        {
            for (int j = 0; j < n; j++)
            {
               System.out.print("*"); 
            }
            System.out.println("");
        }
    }

}
